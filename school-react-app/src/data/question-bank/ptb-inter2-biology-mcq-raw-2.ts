interface OutQuestion2 {
  id: string;
  text: string;
  type: string;
  difficulty: string;
  marks: number;
  chapterId: string;
  chapterTitle: string;
}

export function parsePtBmcqRaw2(): OutQuestion2[] {
  const lines = RAW_MCQ_2.split(/\r?\n/);
  const res: OutQuestion2[] = [];
  let currentChapter = "";
  let currentSubchapterId = "";
  let currentSubchapterTitle = "";
  const chapterHeadingRegex = /^CHAP(?:TER)?\s*(\d+):?\s*(.*)$/i;
  const shortChapterInline = /^CHAP\s*(\d+):\s*(.+)$/i;
  const subchapterRegex = /^(\d+\.\d+)\s+(.+)$/;
  const qRegex = /^(?:Q)?(\d+)\.\s*(.*)$/i;

  for (let i = 0; i < lines.length; i++) {
    const line = lines[i].trim();
    if (!line) continue;
    const chap = line.match(shortChapterInline) || line.match(chapterHeadingRegex);
    if (chap) {
      currentChapter = chap[1];
      currentSubchapterId = currentChapter;
      currentSubchapterTitle = (chap[2] || "").trim();
      continue;
    }
    const sub = line.match(subchapterRegex);
    if (sub) {
      currentSubchapterId = sub[1];
      currentSubchapterTitle = sub[2].trim();
      continue;
    }
    const q = line.match(qRegex);
    if (q) {
      const qNum = q[1];
      const qText = q[2].trim();
      const id = `${currentSubchapterId || currentChapter || 'ch'}-${qNum}`;
      // classify simply: presence of question marks and keywords
      const type = /\?/i.test(qText) ? 'short' : 'long';
      res.push({
        id,
        text: qText,
        type,
        difficulty: 'Medium',
        marks: type === 'short' ? 2 : 5,
        chapterId: currentSubchapterId || currentChapter || '',
        chapterTitle: currentSubchapterTitle || '',
      });
    } else {
      // Some lines are plain questions without numbering — try to detect
      if (line.length > 20 && /\?$/.test(line)) {
        const id = `${currentSubchapterId || currentChapter || 'ch'}-u${res.length+1}`;
        res.push({ id, text: line, type: 'short', difficulty: 'Medium', marks: 2, chapterId: currentSubchapterId||currentChapter||'', chapterTitle: currentSubchapterTitle||'' });
      }
    }
  }
  return res;
}

export default parsePtBmcqRaw2;
